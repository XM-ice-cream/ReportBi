import {
  requestCenter
} from '@/libs/request'
/*panel-additional-recording  接口 */

export const getlistReq = data => {
  return requestCenter({
    timeout: 0,
    baseUrl: window.localStorage.getItem("reportip"),
    url: '/api/reportcenter/repairrefdesapi/v1/repairrefdesapi/getloglist',
    method: 'post',
    data
  })
}

